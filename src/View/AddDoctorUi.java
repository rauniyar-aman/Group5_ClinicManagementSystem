    private boolean validateDoctorForm(String name, String email, String password, String mobile) {
        if (name.isEmpty() || email.isEmpty() || password.isEmpty() || mobile.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "All fields are required.");
            return false;
        }

        if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[A-Za-z]{2,}$")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter a valid email address.");
            return false;
        }

        if (!mobile.matches("\\d{10}") || mobile.startsWith("0")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter a valid 10-digit mobile number without leading 0.");
            return false;
        }

        return true;
    }

