    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String docName = doctor_name.getText().trim();
        String email = email_id.getText().trim();
        String docPass = pass.getText().trim();
        String mobile = mobile_num.getText().trim();
        String specialization = department.getSelectedItem().toString();

        if (!validateDoctorForm(docName, email, docPass, mobile)) {
            return;
        }

        AddDoctorController controller = new AddDoctorController();
        boolean success = controller.addDoctor(docName, email, docPass, mobile, specialization);
        if (success) {
            doctor_name.setText("");
            email_id.setText("");
            pass.setText("");
            mobile_num.setText("");
            department.setSelectedIndex(0);
        }
    }// GEN-LAST:event_jButton1ActionPerformed

