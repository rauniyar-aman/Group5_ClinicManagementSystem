    private void setupPasswordField() {
        // Store original echo character for password masking
        if (pass1 instanceof javax.swing.JPasswordField) {
            originalEchoChar = ((javax.swing.JPasswordField) pass1).getEchoChar();
        }
        // Add listener to checkbox
        showPasswordCheckBox.addActionListener(evt -> togglePasswordVisibility());
    }

    private void togglePasswordVisibility() {
        if (pass1 instanceof javax.swing.JPasswordField) {
            javax.swing.JPasswordField passwordField = (javax.swing.JPasswordField) pass1;
            if (showPasswordCheckBox.isSelected()) {
                // Show password - set echo char to 0 (visible)
                passwordField.setEchoChar((char) 0);
            } else {
                // Hide password - restore original echo char
                passwordField.setEchoChar(originalEchoChar);
            }
        }
    }

