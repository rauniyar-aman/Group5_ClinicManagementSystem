    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String email2 = email1.getText().trim();
        String pass2 = new String(pass1.getPassword()).trim();

        if (email2.isEmpty() || pass2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter both email and password.", "Validation Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        LoginController controller = new LoginController();
        String check = controller.login(email2, pass2);
        System.out.print(check);
        if ("null".equals(check)) {
            JOptionPane.showMessageDialog(null, "Login failed! Please check your credentials.");
        } else if ("admin".equals(check)) {
            AdminUi signWin = new AdminUi();
            signWin.setVisible(true);
            this.dispose();
        } else if ("doctor".equals(check)) {
            Doctor_Dashboard docWin = new Doctor_Dashboard();
            docWin.setVisible(true);
            this.dispose();
        }

        else if ("user".equals(check)) {
            PatientDashUi signWin = new PatientDashUi();
            signWin.setVisible(true);
            this.dispose();
        }
    }// GEN-LAST:event_jButton1ActionPerformed

